def load_historical_data():
    df = pd.read_csv(io.StringIO(TSLA_RAW_CSV.strip()))
    df['Date'] = pd.to_datetime(df['Date'])
    
    # Sort chronologically just in case
    df = df.sort_values('Date').reset_index(drop=True)
    
    # Compute base technical indicators
    # Daily returns
    df['dailyReturn'] = df['Close'].pct_change().fillna(0.0)
    
    # SMA 50 equivalent (window 8 on this smaller scale dataset)
    sma_w = 8
    df['sma50'] = df['Close'].rolling(window=sma_w, min_periods=1).mean().round(2)
    
    # SMA 200 equivalent (window 24 on this dataset)
    lma_w = 24
    df['sma200'] = df['Close'].rolling(window=lma_w, min_periods=1).mean().round(2)
    
    # EMA 20 equivalent (smoothing window 5)
    df['ema20'] = df['Close'].ewm(span=5, adjust=False).mean().round(2)
    
    # Volatility: rolling standard deviation of dailyReturn (window 6)
    vol_w = 6
    df['volatility'] = df['dailyReturn'].rolling(window=vol_w, min_periods=1).std().fillna(0.025).round(4)
    
    # RSI calculation (window 6)
    rsi_w = 6
    delta = df['Close'].diff()
    gain = (delta.where(delta > 0, 0)).rolling(window=rsi_w, min_periods=1).mean()
    loss = (-delta.where(delta < 0, 0)).rolling(window=rsi_w, min_periods=1).mean()
    
    rs = gain / loss
    rsi = 100 - (100 / (1 + rs))
    # Replace NaN or division by zero with 50.0
    df['rsi'] = rsi.fillna(50.0).round(1)
    
    # format columns
    df['Open'] = df['Open'].round(2)
    df['High'] = df['High'].round(2)
    df['Low'] = df['Low'].round(2)
    df['Close'] = df['Close'].round(2)
    
    return df

def get_processed_data(df, date_range):
    # End date cutoff is 2020-02-03
    max_date = df['Date'].max()
    
    if date_range == '1Y':
        cutoff = max_date - pd.DateOffset(months=12)
        df_filtered = df[df['Date'] >= cutoff].copy().reset_index(drop=True)
    elif date_range == '6M':
        cutoff = max_date - pd.DateOffset(months=6)
        df_filtered = df[df['Date'] >= cutoff].copy().reset_index(drop=True)
    elif date_range == '3Y':
        cutoff = max_date - pd.DateOffset(months=36)
        df_filtered = df[df['Date'] >= cutoff].copy().reset_index(drop=True)
    else:
        df_filtered = df.copy()
        
    if df_filtered.empty:
        return df_filtered
        
    # Secondary technical markers
    # MACD calculations
    df_filtered['macdLine'] = ((df_filtered['Close'] - df_filtered['ema20']) * 1.8).round(2)
    df_filtered['signalLine'] = 0.0
    for idx in range(len(df_filtered)):
        if idx > 0:
            df_filtered.loc[idx, 'signalLine'] = round(df_filtered.loc[idx, 'macdLine'] * 0.28 + df_filtered.loc[idx-1, 'Close'] * 0.04, 2)
        else:
            df_filtered.loc[idx, 'signalLine'] = 0.0
    df_filtered['macdHist'] = (df_filtered['macdLine'] - df_filtered['signalLine']).round(2)
    
    # Bollinger Bands
    df_filtered['middleBand'] = df_filtered['ema20']
    df_filtered['stdDev'] = df_filtered['volatility'] * df_filtered['Close'] * 1.35
    df_filtered['upperBand'] = (df_filtered['middleBand'] + df_filtered['stdDev']).round(2)
    df_filtered['lowerBand'] = (df_filtered['middleBand'] - df_filtered['stdDev']).clip(lower=10.0).round(2)
    
    # Compound Returns index (initial $1000 baseline)
    first_close = df_filtered.loc[0, 'Close']
    df_filtered['cumulativeReturn'] = (((df_filtered['Close'] - first_close) / first_close) * 100).round(2)
    df_filtered['compoundGrowth'] = (1000 * (1 + df_filtered['cumulativeReturn'] / 100)).round(2)
    
    # Session spreads
    df_filtered['openCloseSpread'] = (df_filtered['Close'] - df_filtered['Open']).round(2)
    df_filtered['highLowRange'] = (df_filtered['High'] - df_filtered['Low']).round(2)
    
    # Quantitative floors/ceilings
    df_filtered['estSupport'] = (df_filtered['Low'] * 0.975).round(2)
    df_filtered['estResistance'] = (df_filtered['High'] * 1.025).round(2)
    
    # Volatility curves
    df_filtered['dailyReturnPct'] = (df_filtered['dailyReturn'] * 100).round(2)
    df_filtered['absReturnPct'] = df_filtered['dailyReturnPct'].abs()
    df_filtered['shortVol'] = (df_filtered['volatility'] * 100).round(2)
    df_filtered['macroVol'] = (df_filtered['shortVol'] * (0.8 + np.sin(df_filtered.index * 0.1) * 0.15)).round(2)
    
    return df_filtered

def compute_selected_point_signals(df, selected_date):
    if isinstance(selected_date, str):
        selected_date = pd.to_datetime(selected_date)
        
    rows = df[df['Date'] == selected_date]
    if rows.empty:
        # Fallback to last
        row = df.iloc[-1]
    else:
        row = rows.iloc[0]
        
    rsi = row.get('rsi', 50.0)
    ema20 = row.get('ema20', row['Close'])
    sma50 = row.get('sma50', row['Close'])
    sma200 = row.get('sma200', row['Close'])
    
    if rsi >= 70:
        rsi_status = 'Overbought'
        rsi_advice = 'Bearish reversal risk. Trim long positions.'
    elif rsi <= 30:
        rsi_status = 'Oversold'
        rsi_advice = 'Bullish mean-reversion potential. Monitor for accumulation.'
    else:
        rsi_status = 'Neutral Consolidation'
        rsi_advice = 'Stable market momentum.'
        
    trend_status = 'Bullish Trend Bias' if ema20 > sma50 else 'Bearish Trend Bias'
    macro_alignment = 'Golden Cross Structure' if sma50 > sma200 else 'Death Cross Structure'
    
    score = 50
    if rsi <= 32:
        score += 20
    elif rsi >= 68:
        score -= 20
        
    if ema20 > sma50:
        score += 15
    else:
        score -= 15
        
    if sma50 > sma200:
        score += 15
    else:
        score -= 15
        
    if row.get('dailyReturn', 0.0) > 0:
        score += 10
    else:
        score -= 10
        
    score = max(15, min(95, score))
    
    if score >= 70:
        recommendation = 'STRONG BUY'
    elif score >= 55:
        recommendation = 'ACCUMULATE / BUY'
    elif score >= 45:
        recommendation = 'HOLD & WATCH'
    elif score >= 30:
        recommendation = 'REDUCE / SELL'
    else:
        recommendation = 'STRONG SELL / HEDGE'
        
    return {
        'rsiStatus': rsi_status,
        'rsiAdvice': rsi_advice,
        'trendStatus': trend_status,
        'macroAlignment': macro_alignment,
        'score': score,
        'recommendation': recommendation,
        'support': round(row['Low'] * 0.975, 2),
        'resistance': round(row['High'] * 1.025, 2),
        'rsiRaw': rsi,
        'ema20Raw': ema20,
        'sma50Raw': sma50,
        'sma200Raw': sma200,
        'close': row['Close'],
        'open': row['Open'],
        'high': row['High'],
        'low': row['Low'],
        'volume': row['Volume'],
        'date': row['Date'].strftime('%Y-%m-%d')
    }
